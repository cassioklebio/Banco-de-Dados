CREATE DATABASE CONSULTORIO;

USE DATABASE CONSULTORIO;

CREATE TABLE AMBULATORIO(
	NROA INT NOT NULL AUTO_INCREMENT,
	ANDAR INT(2) NOT NULL,
	CAPACIDADE INT (10000) NOT NULL,
	PRIMARY KEY(NROA);
	);

CREATE TABLE MEDICO(
	CODM INT NOT NULL AUTO_INCREMENT,
	NOME VARCHAR(30) NOT NULL,
	IDADE INT(2) NOT NULL,
	ESPECIALIDADE VARCHAR(30) NOT NULL,
	PRIMARY KEY(CODM),
	FOREIGN KEY(NROA) REFERENCES AMBULATORIO(NROA)
	);

CREATE TABLE PACIENTE(
	CODP INT NOT NULL AUTO_INCREMENT,
	NOME VARCHAR(30) NOT NULL,
	IDADE INT(2) NOT NULL,
	DOENCA VARCHAR(30) NOT NULL,
	PRIMARY KEY(CODP)
	);

CREATE TABLE CONSULTORIO(
        ID_CONSULTORIO INT NOT NULL AUTO_INCREMENT,
        CODM INT,
	CODP INT,
	DATA DATE(10) NOT NULL,
	HORA TIME(10) NOT NULL,
	FOREIGN KEY(CODM) REFERENCES MEDICO(CODM),
	FOREIGN KEY(CODP) REFERENCES PACIENTE(CODP)
	);

CREATE TABLE FUNCIONARIO(
	CODF INT NOT NULL AUTO_INCREMENT,
	NOME VACHAR(50) NOT NULL,
	IDADE INT(2) NOT NULL,
	CIDADE VARCHAR(30) NOT NULL,
	SALARIO FLOAT(10) NOT NULL,
	PRIMARY KEY(CODF)
	);	

CREATE TABLE ENDERECO(
	ID_ENDERECO INT NOT NULL AUTO_INCREMENT,
	ID_MEDICO INT,
	ID_PACIENTE INT,
	RUA VARCHAR(50) NOT NULL,
	NUMERO VARCHAR(10),
	BAIRRO VARCHAR(20),
	CIDADE VARCHAR(20),
	ESTADO VARCHAR(2),
	PAIS VARCHAR(10),
	PRIMARY KEY (ID_ENDERENCO),
	FOREIGN KEY(CODM) REFERENCES MEDICO(CODM),
	FOREIGN KEY(CODP) REFERENCES PACIENTE(CODP)
	);
	
	
	
	
	
	
